def char_to_blocks(char, font='block'):
    """ÊñáÂ≠ó„Çí„Éñ„É≠„ÉÉ„ÇØÈÖçÂàó„Å´Â§âÊèõ"""
    char = char.upper()
    if char in FONTS[font]:
        return FONTS[font][char]
    return ['      ' for _ in range(6)]

def text_to_ascii_art(text, style='block'):
    """„ÉÜ„Ç≠„Çπ„Éà„ÇíASCII„Ç¢„Éº„Éà„Å´Â§âÊèõ"""
    lines = [[] for _ in range(6)]
    
    for char in text:
        blocks = char_to_blocks(char, style)
        for i in range(6):
            lines[i].append(blocks[i])
    
    # Ë°å„ÇíÁµêÂêà
    result = []
    for line in lines:
        result.append(''.join(line))
    
    return '\n'.join(result)

def add_frame(art, style='simple'):
    """„Éï„É¨„Éº„É†„ÇíËøΩÂä†"""
    lines = art.split('\n')
    width = len(lines[0]) if lines else 0
    
    if style == 'simple':
        border = '‚îÄ' * width
        return f'‚îå{border}‚îê\n' + '\n'.join(f'‚îÇ{line}‚îÇ' for line in lines) + f'\n‚îî{border}‚îò'
    
    elif style == 'double':
        border = '‚ïê' * width
        return f'‚ïî{border}‚ïó\n' + '\n'.join(f'‚ïë{line}‚ïë' for line in lines) + f'\n‚ïö{border}‚ïù'
    
    elif style == 'stamp':
        top = '‚ïî' + '‚ïê' * width + '‚ïó'
        bottom = '‚ïö' + '‚ïê' * width + '‚ïù'
        result = [top]
        for line in lines:
            result.append(f'‚ïë {line} ‚ïë')
        result.append(bottom)
        # „Çπ„Çø„É≥„Éó„Éû„Éº„ÇØ
        result.append('    ‚òÜ STAMP ‚òÜ')
        return '\n'.join(result)
    
    return art

def create_stamp(text, frame_style='stamp'):
    """„Çπ„Çø„É≥„Éó„Çí‰ΩúÊàê"""
    art = text_to_ascii_art(text)
    return add_frame(art, frame_style)

